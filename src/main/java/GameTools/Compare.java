package GameTools;

/**
 * 动画过渡条件比较
 *
 * @author ZYM
 */
public enum Compare {
    /**
     * Bool等于
     */
    BoolEqual,
    /**
     * Bool不等于
     */
    BoolNotEqual,
    /**
     * Double大于
     */
    DoubleGreaterThan,
    /**
     * Double大于等于
     */
    DoubleGreaterEqualThan,
    /**
     * Double等于
     */
    DoubleEqual,
    /**
     * Double小于等于
     */
    DoubleLessEqualThan,
    /**
     * Double小于
     */
    DoubleLessThan,
    /**
     * int大于
     */
    IntGreaterThan,
    /**
     * int大于等于
     */
    IntGreaterEqualThan,
    /**
     * int等于
     */
    IntEqualThan,
    /**
     * int小于等于
     */
    IntLessEqualThan,
    /**
     * int小于
     */
    IntLessThan,
}